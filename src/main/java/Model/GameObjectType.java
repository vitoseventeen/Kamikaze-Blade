package Model;

/**
 * Enumeration representing different types of game objects.
 */
public enum GameObjectType {
    CHEST,      // Represents a chest game object.
    KEY,        // Represents a key game object.
    DOOR,       // Represents a door game object.
    NPC,        // Represents an NPC (Non-Playable Character) game object.
    COIN,       // Represents a coin game object.
    POTION,     // Represents a potion game object.
    QUEST_KEY,  // Represents a quest key game object.
    LEVELDOOR,  // Represents a level door game object.
    HEAL        // Represents a heal game object.
}
